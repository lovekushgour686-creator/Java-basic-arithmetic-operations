/*Question

Write a Java program to find the area of a rectangle. */
public class program04 {
    public static void main(String[] args) {
        int len = 10;
        int wid = 20;
        int Area = len * wid;
        System.out.println("Area of Rectangle ==> ");
        System.out.println(len + " * " + wid + " = " + Area);
    }
}